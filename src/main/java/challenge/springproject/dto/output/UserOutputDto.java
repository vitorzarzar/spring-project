package challenge.springproject.dto.output;

import challenge.springproject.dto.input.PhoneDto;

import java.time.LocalDateTime;
import java.util.List;

public class UserOutputDto {
    private Long id;

    private String name;

    private String email;

    private String password;

    private List<PhoneDto> phones;

    private LocalDateTime created;

    private LocalDateTime lastLogin;

    private String token;

    public UserOutputDto() {
    }

    public UserOutputDto(Long id, String name, String email, String password, List<PhoneDto> phones, LocalDateTime created, LocalDateTime lastLogin, String token) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = password;
        this.phones = phones;
        this.created = created;
        this.lastLogin = lastLogin;
        this.token = token;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public List<PhoneDto> getPhones() {
        return phones;
    }

    public void setPhones(List<PhoneDto> phones) {
        this.phones = phones;
    }

    public LocalDateTime getCreated() {
        return created;
    }

    public void setCreated(LocalDateTime created) {
        this.created = created;
    }

    public void setLastLogin(LocalDateTime lastLogin) {
        this.lastLogin = lastLogin;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
